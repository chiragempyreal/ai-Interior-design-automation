  pw.Widget _buildPDFInfoRow(String label, String value) {
    return pw.Row(
      mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
      children: [
        pw.Text(
          label,
          style: pw.TextStyle(
            fontSize: 11,
            fontWeight: pw.FontWeight.bold,
            color: PdfColors.grey700,
          ),
        ),
        pw.Text(
          value,
          style: pw.TextStyle(fontSize: 11, color: PdfColors.grey900),
        ),
      ],
    );
  }

  List<pw.Widget> _buildBudgetBreakdown() {
    final totalBudget = project.technicalRequirements.budget.totalBudget;

    // Calculate itemized breakdown (percentages based on typical interior design projects)
    final materials = totalBudget * 0.35; // 35% for materials
    final labor = totalBudget * 0.25; // 25% for labor
    final furniture = totalBudget * 0.20; // 20% for furniture
    final lighting = totalBudget * 0.10; // 10% for lighting & electrical
    final design = totalBudget * 0.10; // 10% for design services

    final items = [
      {'name': 'Materials & Finishes', 'amount': materials},
      {'name': 'Labor & Installation', 'amount': labor},
      {'name': 'Furniture & Fixtures', 'amount': furniture},
      {'name': 'Lighting & Electrical', 'amount': lighting},
      {'name': 'Design Services', 'amount': design},
    ];

    final widgets = <pw.Widget>[];

    for (var item in items) {
      widgets.add(
        pw.Padding(
          padding: const pw.EdgeInsets.only(bottom: 8),
          child: pw.Row(
            mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
            children: [
              pw.Text(
                item['name'] as String,
                style: pw.TextStyle(fontSize: 11, color: PdfColors.grey700),
              ),
              pw.Text(
                '‚Çπ${((item['amount'] as double) / 1000).toStringAsFixed(0)}k',
                style: pw.TextStyle(
                  fontSize: 11,
                  fontWeight: pw.FontWeight.bold,
                  color: PdfColors.grey900,
                ),
              ),
            ],
          ),
        ),
      );
    }

    // Add divider and total
    widgets.add(pw.Divider(color: PdfColors.grey400));
    widgets.add(
      pw.Padding(
        padding: const pw.EdgeInsets.only(top: 8),
        child: pw.Row(
          mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
          children: [
            pw.Text(
              'TOTAL BUDGET',
              style: pw.TextStyle(
                fontSize: 12,
                fontWeight: pw.FontWeight.bold,
                color: PdfColors.grey800,
              ),
            ),
            pw.Text(
              '‚Çπ${(totalBudget / 100000).toStringAsFixed(1)}L',
              style: pw.TextStyle(
                fontSize: 13,
                fontWeight: pw.FontWeight.bold,
                color: PdfColors.grey900,
              ),
            ),
          ],
        ),
      ),
    );

    return widgets;
  }

  Future<Uint8List?> _getImageBytes(String imageUrl) async {
    try {
      if (imageUrl.startsWith('http')) {
        // Network image
        final response = await http.get(Uri.parse(imageUrl));
        if (response.statusCode == 200) {
          return response.bodyBytes;
        }
      } else {
        // Local file
        final file = File(imageUrl);
        if (await file.exists()) {
          return await file.readAsBytes();
        }
      }
    } catch (e) {
      print("Error loading image: $e");
    }
    return null;
  }
